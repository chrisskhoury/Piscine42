int		ft_iterative_factorial(int nb)
{
	int fac;

	fac = 1;
	if (nb < 0 || nb >= 13)
		fac = 0;
	else
		while (nb > 0 && nb < 13)
		{
			fac *= nb;
			nb--;
		}
	return (fac);
}
